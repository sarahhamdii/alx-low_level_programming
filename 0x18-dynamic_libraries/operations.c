/**
 * add - add numbers
 * @x: first number
 * @y: second number
 * Return: the result
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - sub numbers
 * @x: first number
 * @y: second number
 * Return: the result
 */
int sub(int x, int y)
{
	return (x + y);
}

/**
 * mul - mul numbers
 * @x: first number
 * @y: second number
 * Return: the result
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - div numbers
 * @x: first number
 * @y: second number
 * Return: the result
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - mod numbers
 * @x: first number
 * @y: second number
 * Return: the result
 */
int mod(int x, int y)
{
	return (x % y);
}
